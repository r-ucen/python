import math

pi = 3.14

x = 1
y = 2
z = 3

print(round(pi)) # rounds to the nearest whole integer
print(math.ceil(pi)) # rounds up
print(math.floor(pi)) # rounds down
print(abs(pi)) # absolute value
print(pow(pi, 2)) # pi to the power of 2
print(math.sqrt(pi)) # square root

print(max(x, y, z)) # returns the highest value
print(min(x, y, z)) # returns the lowest value