name = "bro Code"

print(len(name)) # length of string
print(name.find("b")) # find an index of a character
print(name.capitalize()) # capitalizes the first character
print(name.upper()) # capitalizes whole string
print(name.lower()) # everything lowercase
print(name.isdigit()) # returns true or false depending on the fact whether its digit or not
print(name.isalpha()) # returns true if the string contains ONLY alphabetical characters
print(name.count("o")) # counts how many selected characters the string contains
print(name.replace("o", "e")) # first is the character you want to replace and second is which character should replace the character
print(name * 3) # prints the string n times