# type casting = convert to another data type

x = 1   #int
y = 2.0 #float
z = "3" #str

# to int

y = int(y)
z = int(z)

print(x)
print(y)
print(z * 3)

# to float

x = float(x)
y = float(y)
z = float(z)

print(x)
print(y)
print(z * 3)

# to string

x = str(x)
y = str(y)
z = str(z)

print(x)
print(y)
print(z * 3)